import re

# 例子1：匹配网址

ip = '12.23.84.dsfa.23s.3234~lsjfw+23sfaf192.168.32.43_w342d~@#9436'
ret=re.findall("[0-9]{1,3}",ip)
print(ret)
# ['12', '23', '84', '23', '323', '4', '23', '192', '168', '32', '43', '342', '943', '6']

ret=re.findall("[0-9]{1,3}\.[0-9]{1,3}",ip)
print(ret)
# ['12.23', '192.168', '32.43']

ret=re.findall("[0-9]{1,3}\.[0-9]{1,3}\.[0-9]{1,3}",ip)
print(ret)
# ['12.23.84', '192.168.32']

ret=re.findall("[0-9]{1,3}\.[0-9]{1,3}\.[0-9]{1,3}\.[0-9]{1,3}",ip)
print(ret)
# ['192.168.32.43']

# 例子2
ret=re.findall("(\d+)",ip)
print(ret)
# ['12', '23', '84', '23', '3234', '23', '192', '168', '32', '43', '342', '9436']

ret=re.findall("(\.+\d+){1,3}",ip)
print(ret)
# ['.84', '.23', '.3234', '.43']

ret=re.findall("(?:\.+\d+){1,3}",ip)   #?:表示匹配括号的那一组数据，必须连着
print(ret)
# ['.23.84', '.23', '.3234', '.168.32.43']

ret=re.findall("[0-9]{1,3}(?:\.+\d+){3}",ip)
print(ret)
# ['192.168.32.43']

# 法3：
ret=re.findall("(?:\d+\.+){3}\d{1,3}",ip)
print(ret)
# ['192.168.32.43']

# 法4
ret=re.findall("(?:\d{1,3}\.){3}\d{1,3}",ip)
print(ret)
# ['192.168.32.43']
